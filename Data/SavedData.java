package Data;

public class SavedData {
    
    private String name;
    
    // Constructor to accept a name
    public SavedData(String name) {
        this.name = name;
    }
    
    // Getters and Setters for name (optional, if you want to access it later)
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
}
